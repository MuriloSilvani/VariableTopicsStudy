
public class PontoFlutuante {
	
	public static void main(String[] args) {
		
		double salario;
		salario = 1250.70;
		
		System.out.println("Salário: " + salario);
		
		double divisao = 3.14 / 2;
		
		System.out.println("Divisão: " + divisao);
		
		int divisaoInteira = 5 / 2;
		
		System.out.println("Divisão inteira: " + divisaoInteira);
		
		double tentativaDivisaoFloat;
		
		tentativaDivisaoFloat = 5 / 2;
		System.out.println("Divisão 5/2: " + tentativaDivisaoFloat);
		

		tentativaDivisaoFloat = 5.0 / 2;
		System.out.println("Divisão 5.0/2: " + tentativaDivisaoFloat);
	}
}
